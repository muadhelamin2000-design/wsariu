  static Map<String, Map<String, String>> getDailyInspirationTriple() {
    final now = DateTime.now();
    final dayIndex = (now.year * 365 + now.month * 30 + now.day);
    
    return {
      'verse': verses[dayIndex % verses.length],
      'hadith': hadiths[dayIndex % hadiths.length],
      'saying': sayings[dayIndex % sayings.length],
    };
  }

  static Map<String, String> getRotatingNotificationMessage() {
    final box = Hive.box(boxName);
    int index = box.get('notification_index', defaultValue: 0);
    
    final all = [...verses, ...hadiths];
    final message = all[index % all.length];
    
    box.put('notification_index', (index + 1) % all.length);
    return message;
  }
